// Подключаем встроенный модуль path для работы с путями
const path = require('path');

// Подключаем установленный плагин
const HtmlWebpackPlugin = require('html-webpack-plugin');

module.exports = {
    // Режим сборки: 'development' — для разработки (быстрая сборка, source maps, не минифицировано)
    mode: 'development',

    // Точка входа: главный JS файл, от которого Webpack начнёт анализ зависимостей
    entry: './src/index.js',

    // Настройки выходного файла
    output: {
        // Имя собранного JS-файла
        filename: 'bundle.js',
        // Абсолютный путь к папке, куда складывать результат
        path: path.resolve(__dirname, 'dist'),
        // Очищать папку dist перед каждой новой сборкой (чтобы не оставалось старых файлов)
        clean: true,
    },

    // Настройки сервера разработки (запускается командой webpack serve)
    devServer: {
        // Папка, из которой сервер отдаёт статику (результат сборки)
        static: './dist',
        // Включить горячую замену модулей (Hot Module Replacement)
        hot: true,
        // Порт, на котором будет доступен сервер
        port: 3000,
        // Автоматически открывать браузер после запуска
        open: true,
    },

    // Плагины — расширяют возможности Webpack
    plugins: [
        new HtmlWebpackPlugin({
            // Шаблон HTML, который будет использоваться как основа
            template: './src/index.html',
            // Заголовок страницы (можно использовать переменную в шаблоне)
            title: 'Игровой движок',
        }),
    ],

    // Правила обработки различных типов файлов
    module: {
        rules: [
            {
                // Регулярное выражение: все файлы, заканчивающиеся на .scss (регистр не важен)
                test: /\.scss$/i,
                // Какие загрузчики применить (применяются справа налево)
                use: [
                    'style-loader',   // 3) Внедряет CSS в DOM
                    'css-loader',     // 2) Превращает CSS в JavaScript-модуль
                    'sass-loader'     // 1) Компилирует SCSS в CSS
                ],
            },
        ],
    },
};